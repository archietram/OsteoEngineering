         <p>This Program Project will continue to investigate Photodynamic Therapy (PDT) as applied to solid tumor therapy. The clinical applications will focus on malignant brain tumors and prostate cancer. The four projects will cover research topics ranging from basic molecular and cellular responses to PDT to the conduct of novel clinical trials employing PDT. The projects are supported by two cores that are integral to the overall success of the Program. The synergy of the Program will continue to depend upon the effective interactions between both the scientific and clinical projects and the investigators. Project 1 will investigate in preclinical models novel strategies for the improved treatment of brain tumors. This project will incorporate aim one from prior Project 4 examining and comparing the molecular effects of ALA-mediated metronomic PDT versus acute ALA-mediated PDT in brain tumor. Project 2 consists of both ongoing and new clinical trials for the treatment of brain tumors. Project 3 will investigate in canine and murine models the possible use of PDT for the treatment of prostate tumors employing a novel, second-generation sensitizer, Tookad. Project 5 (not renumbered) will focus on the development of new biophysical techniques for monitoring PDT in vivo and on translating already developed methods and devices into clinical applications. Core 1 will provide the necessary administrative support for the overall Program and Core 2 will provide technical support to all of the preclinical and clinical projects. </p> 


