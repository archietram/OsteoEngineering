<p><strong><img src="img/research/bonescaffold.png" alt="bone scaffold" style="float:right">National Institutes of Health: 2R01DE013740-06A1:</strong><em>"Strength and Resorption of Biodegradable Skull Implants"</em> (D. Dean, Principal Investigator)</p>
         <p>Large skull defects arise frequently following neurosurgery and craniofacial reconstructive surgery for trauma, cancer resection, and congenital deformity. Concerns over revascularization, aesthetics and fixation of the implant are magnified by the post-surgical need to protect the underlying brain from both trauma and infection. Tissue engineered bone replacements have been shown to ossify small skeletal defects with potential utility in fracture site repair, but currently, these materials do not effectively address skull defects large enough to require graft or prosthetic augmentation. We propose to: (1) use Stereolithographic (SLA) rendering (i.e., rapid prototyping) of poly(propylene fumarate)/b-tricalcium phosphate (PPF/b-TCP) to produce porous implants with computer-modeled mechanical, geometric, cell attachment (i.e., in vitro in a bioreactor, or in vivo), and resorption properties; (2) use a nude rat intra-muscular pocket (i.e., bone ossicle) model to test the in vivo interaction of PPF/b-TCP, canine osteoprogenitor cells, and growth factors; (3) select the best performing (i.e., rate and strength of bone produced) construct to elevate to a critical size (i.e., 2.5 cm) and then a super-critical size (i.e., 4.0 cm) canine cranial implant. In order to accomplish these goals we propose the following specific aims: (1) Determine an effective pore geometry and surface texture for cRBM and/or cMSC loading and seeding of SLA-rendered, porous PPF/b-TCP scaffolds. (2) Determine the most osteogenic combination of the best AIM 1 scaffolds (i.e., PPF geometry, PPF texture, cRBM, or cMSCs), bioreactor or in vivo cell culture, and autologous (platelets, cRBM) and/or exogenous (TGF-b2, BMP-2, BMP-7, FGF-2 and VEGF) growth factors in a nude rat intra-muscular bone ossicle model. Subaim: Determine whether pre-implantation bioreactor culturing improves rate and quality of bone formation. (3) Determine whether the best performing bone ossicle strategy for the nude rat produces reliable infilling of initially critical size (2.5 cm) circular, biparietal, cranial implants in the dog. A second cycle of Aims 1-3 in years 4-5 will use what is learned in Cycle I to maximally expand the area of reliably grown bone from 2.5 to 4.0 cm or larger. Unlike current standard of care PMMA and titanium cranial implants, reliable resorption and bone formation in PPF/b-TCP scaffolds will serve to seal off the cranial cavity from infection, better translate traumatic stress to the rest of the skull, and remodel with the rest of the skull over time. </p> 

