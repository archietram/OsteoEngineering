         <p>Project 4: Aim: To assess the efficacy of stereolithographic (SLA) 3D printing (i.e., rapid prototyping) of biodegradable implants. Abstract: The complex geometry of cranial bone defects presents a serious surgical challenge. Preoperative fabrication of implants that match the defect geometry greatly improves fixation and the cosmetic result. Dr. Dean has developed software to design patient-specific implants from 3D CT scans that can then be rendered using a stereolithographic (SLA) prototyping device. Dr. Dean and others have patented a new STL method for direct production of impants via the photo-polymerization of (poly)propylene fumarate (PPF) or other resorbable polymers. This project seeks to establish the efficacy and commercial potential for the rapid prototyping of light polymerizable tissue engineering scaffold materials.</p> 

