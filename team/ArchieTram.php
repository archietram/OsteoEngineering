			<p>Abstract:</p>
			<p>Perfusion of 3D-printed bone tissue engineering scaffolds enhances osteogenesis due to increased nutrient and waste transport. Additionally, flow-induced wall shear stress contributes to osteogenic differentiation and mechanical degradation of scaffolds. Therefore, it is important to quantify the fluid mechanics present in bioreactor systems that are used to perfuse scaffolds. This study aims to provide a method for numerical simulation of fluid in a bioreactor and to estimate wall shear stress on scaffolds within the system. A computational model of the bioreactor/scaffold system was designed to mimic fluid mechanics in vitro. CAD models of scaffolds with porosity derived from Schoenâ€™s Triply Periodic Minimal Surfaces (TPMS) were perfused, in silico, in a CAD model of a bioreactor. Fluid mechanics of the bioreactor and shear stresses on the scaffold will be recorded and analyzed.  In addition, the degradation of these scaffolds will be experimentally determined to correlate with the results obtained from simulation. This study will shed light on useful designs for resorbable bone tissue engineering grafts.</p>
			
